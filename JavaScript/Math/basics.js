// To use pi
console.log(Math.PI)


// to round the number
let num = 10.5;
console.log(Math.round(num))

// to get power
console.log(Math.pow(4,2))
console.log(Math.pow(3.6,2))


// to get square root
console.log(Math.sqrt(45))
console.log(Math.sqrt(36))


// to get absolute value
console.log(Math.abs(-5))
console.log(Math.abs(4-14))


// ceil and floor value
console.log(Math.ceil(4.5))
console.log(Math.floor(4.5))


// to get minimum and maximum values
console.log(Math.min(0, 60, 4, -7, -90, -45))
console.log(Math.max(0, 60, 4, -7, -90, -45))


// to get random number between 1 to 10
console.log(Math.ceil(Math.random()*10))


// trunc: return integer part of the number
console.log(Math.trunc(6.7))
console.log(Math.trunc(-6.7))